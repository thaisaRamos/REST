package rest;

public class Artista {
	

	private int rank;
	private String name;
	private String ouvinte;
	
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getOuvinte() {
		return ouvinte;
	}
	public void setOuvinte(String ouvinte) {
		this.ouvinte = ouvinte;
	}
	
	
	
	
	

}
